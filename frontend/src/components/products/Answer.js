import React from 'react'
import { useSelector, useDispatch } from 'react-redux'
import NewAnswer from './NewAnswer'
import '../../styles/QuestionsAnswers.css'
import Modal from '../Modal'
import DeleteAnswer from './DeleteAnswer'
import { setModalDeleteAnswer } from '../../features/modalSlice'
import { setIdDeleteAnswer } from '../../features/editSlice'
export default function Answer({ answerQuestion, id, idAnswer }) {
    const dispatch = useDispatch()
    const modalDeleteAnswer = useSelector(state => state.modal.modalDeleteAnswer)
    const user = useSelector(state => state.logged.user)
    return (
        <div className='answer-container'>
            <div>
                {
                    (user?.role === "admin" && answerQuestion) &&
                    <button className='delete' type="button" onClick={() => {
                        dispatch(setModalDeleteAnswer())
                        dispatch(setIdDeleteAnswer(idAnswer))
                    }}
                    >
                        <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-trash" viewBox="0 0 16 16">
                            <path d="M5.5 5.5A.5.5 0 0 1 6 6v6a.5.5 0 0 1-1 0V6a.5.5 0 0 1 .5-.5zm2.5 0a.5.5 0 0 1 .5.5v6a.5.5 0 0 1-1 0V6a.5.5 0 0 1 .5-.5zm3 .5a.5.5 0 0 0-1 0v6a.5.5 0 0 0 1 0V6z" />
                            <path fill-rule="evenodd" d="M14.5 3a1 1 0 0 1-1 1H13v9a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2V4h-.5a1 1 0 0 1-1-1V2a1 1 0 0 1 1-1H6a1 1 0 0 1 1-1h2a1 1 0 0 1 1 1h3.5a1 1 0 0 1 1 1v1zM4.118 4 4 4.059V13a1 1 0 0 0 1 1h6a1 1 0 0 0 1-1V4.059L11.882 4H4.118zM2.5 3V2h11v1h-11z" />
                        </svg>
                    </button>
                }
            </div>
            {
                answerQuestion ? <p className='answer'>
                    <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-arrow-return-right" viewBox="0 0 16 16">
                        <path fill-rule="evenodd" d="M1.5 1.5A.5.5 0 0 0 1 2v4.8a2.5 2.5 0 0 0 2.5 2.5h9.793l-3.347 3.346a.5.5 0 0 0 .708.708l4.2-4.2a.5.5 0 0 0 0-.708l-4-4a.5.5 0 0 0-.708.708L13.293 8.3H3.5A1.5 1.5 0 0 1 2 6.8V2a.5.5 0 0 0-.5-.5z" />
                    </svg>
                    {answerQuestion}
                </p> : (
                    user?.role === "admin" ? <NewAnswer id={id} />
                        :
                        <p className='answer no answer'>
                            <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-arrow-return-right" viewBox="0 0 16 16">
                                <path fill-rule="evenodd" d="M1.5 1.5A.5.5 0 0 0 1 2v4.8a2.5 2.5 0 0 0 2.5 2.5h9.793l-3.347 3.346a.5.5 0 0 0 .708.708l4.2-4.2a.5.5 0 0 0 0-.708l-4-4a.5.5 0 0 0-.708.708L13.293 8.3H3.5A1.5 1.5 0 0 1 2 6.8V2a.5.5 0 0 0-.5-.5z" />
                            </svg>
                            The seller has not responded
                        </p>
                )
            }
            {
                modalDeleteAnswer &&
                <Modal>
                    <DeleteAnswer />
                </Modal>
            }
        </div>
    )
}
